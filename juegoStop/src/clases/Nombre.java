package clases;

/**
 *
 * @author Estudiante
 */
public class Nombre {
    private String nombre;
    private int codNombre;
    
    public Nombre(){
        
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the codNombre
     */
    public int getCodNombre() {
        return codNombre;
    }

    /**
     * @param codNombre the codNombre to set
     */
    public void setCodNombre(int codNombre) {
        this.codNombre = codNombre;
    }
    
    
}
